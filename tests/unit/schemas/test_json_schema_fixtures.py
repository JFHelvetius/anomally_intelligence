def _find_witness_fixture() -> Path | None:
    """Locate any witness attestation .json in the repo's dev_archive.

    Returns the first match, or None if none are present."""
    base = REPO_ROOT / "dev_archive" / "transparency" / "witnesses"
    if not base.is_dir():
        return None
    for p in base.rglob("*.json"):
        return p
    return None


