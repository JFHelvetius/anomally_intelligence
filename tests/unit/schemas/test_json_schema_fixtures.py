"""Sanity tests for the public JSON Schema 2020-12 contracts in
``docs/schemas/``.

The schemas are the public interop surface (see ``docs/schemas/README.md``).
They are NOT the canonical implementation — Python dataclasses and the
Python verifiers are — but they must stay aligned. These tests catch
drift in two directions:

1. Every schema is itself a valid Draft 2020-12 metaschema (no typos in
   keywords, refs resolve).
2. Representative fixtures generated by the canonical implementation
   validate cleanly against their schema. If the implementation adds a
   field or tightens a value, the schema must be updated in lockstep or
   this test fails.

We deliberately do NOT add a generic property-based test ("any output of
the implementation must validate against the schema") because the
schemas are structural-only — semantic invariants (DAG acyclicity, chain
linkage, etc.) live in the verifiers, not the schema, by design.
"""

from __future__ import annotations

import datetime as dt
import json
from collections.abc import Callable
from pathlib import Path

import pytest
from jsonschema import Draft202012Validator

from aip import Archive
from aip.attestation import generate_keypair, serialize_public_key_pem
from aip.audit import log as audit_log
from aip.core.evidence import EvidenceKind
from aip.core.source import AuthorityLevel, SourceKind
from aip.transparency.key_declaration import (
    WitnessSeed,
    init_declaration,
    operator_public_key_path,
)
from aip.transparency.store import TRANSPARENCY_DIRNAME

REPO_ROOT = Path(__file__).resolve().parents[3]
SCHEMA_DIR = REPO_ROOT / "docs" / "schemas"
KEY_DECLARATION_SCHEMA = SCHEMA_DIR / "key-declaration.v1.schema.json"
INFERENCE_PROOF_SCHEMA = SCHEMA_DIR / "inference-proof.v1.schema.json"
AUDIT_ENTRY_SCHEMA = SCHEMA_DIR / "audit-entry.v1.schema.json"
TRANSPARENCY_MANIFEST_SCHEMA = SCHEMA_DIR / "transparency-manifest.v1.schema.json"
CAPTURE_CERT_SCHEMA = SCHEMA_DIR / "capture-certificate.v1.schema.json"
WITNESS_ATTESTATION_SCHEMA = SCHEMA_DIR / "witness-attestation.v1.schema.json"

UTC = dt.UTC
CANONICAL_TS = dt.datetime(2026, 6, 10, 0, 0, 0, tzinfo=UTC)


def _load_schema(path: Path) -> dict[str, object]:
    data: dict[str, object] = json.loads(path.read_text(encoding="utf-8"))
    return data


def _fixed_clock(ts: dt.datetime) -> Callable[[], dt.datetime]:
    return lambda: ts


# --------------------------------------------------------------- meta


def test_all_schemas_are_valid_draft_2020_12_metaschemas() -> None:
    """Each schema file in docs/schemas/ must itself be a valid Draft 2020-12
    schema. Catches typos in keywords ('require' vs 'required', '$Ref' vs
    '$ref') that would silently allow anything to validate."""
    for path in sorted(SCHEMA_DIR.glob("*.schema.json")):
        schema = _load_schema(path)
        # Draft202012Validator.check_schema raises if the schema is malformed.
        Draft202012Validator.check_schema(schema)


def test_every_schema_has_id_and_schema_keywords() -> None:
    """Hard requirement for stable interop: each schema must declare both
    '$schema' (so validators know which draft) and '$id' (so external
    consumers can pin to a stable URL even before publication)."""
    for path in sorted(SCHEMA_DIR.glob("*.schema.json")):
        schema = _load_schema(path)
        assert schema.get("$schema") == (
            "https://json-schema.org/draft/2020-12/schema"
        ), f"{path.name} missing or wrong $schema"
        sid = schema.get("$id", "")
        assert isinstance(sid, str) and sid.startswith("https://"), (
            f"{path.name} missing or non-URL $id"
        )


# --------------------------------------------------------------- key declaration


def test_minimal_key_declaration_validates(archive_root: Path) -> None:
    """The minimum-shaped declaration produced by ``init_declaration`` is the
    same one the receptor sees in the report. It must validate."""
    # Seed an operator pubkey on disk so init can compute the fingerprint.
    _, pub = generate_keypair()
    pem = serialize_public_key_pem(pub)
    (archive_root / TRANSPARENCY_DIRNAME).mkdir(parents=True)
    operator_public_key_path(archive_root).write_bytes(pem)

    decl = init_declaration(archive_root, operator_id="op-x")
    schema = _load_schema(KEY_DECLARATION_SCHEMA)
    Draft202012Validator(schema).validate(decl)


def test_key_declaration_with_witnesses_validates(
    archive_root: Path,
) -> None:
    _, pub = generate_keypair()
    (archive_root / TRANSPARENCY_DIRNAME).mkdir(parents=True)
    operator_public_key_path(archive_root).write_bytes(
        serialize_public_key_pem(pub)
    )

    decl = init_declaration(
        archive_root,
        operator_id="op-x",
        first_published_at="2026-06-01T00:00:00Z",
        witnesses=(WitnessSeed("alice", "a" * 64), WitnessSeed("bob", "b" * 64)),
    )
    # Add a reference manually so the witness block has all the fields the
    # schema describes (not just the empty external_references list).
    decl["witnesses"][0]["external_references"].append(
        {"kind": "https_pem", "uri": "https://alice.example/k.pem", "note": "n"}
    )
    schema = _load_schema(KEY_DECLARATION_SCHEMA)
    Draft202012Validator(schema).validate(decl)


