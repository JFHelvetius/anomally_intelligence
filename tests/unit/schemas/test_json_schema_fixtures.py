# --------------------------------------------------------------- audit entry


def _ingest(archive_root: Path, blob_path: Path) -> str:
    archive = Archive.open(archive_root)
    ev = archive.ingest_evidence(
        blob_path,
        source_id="schema-test-src",
        source_name="Schema Test Source",
        source_kind=SourceKind.GOVERNMENT_ARCHIVE,
        source_authority=AuthorityLevel.SECONDARY,
        source_jurisdiction="US",
        source_license="public_domain",
        evidence_kind=EvidenceKind.DOCUMENT_SCAN,
        mime_type="application/pdf",
        ingested_by="@schema-test",
        clock=_fixed_clock(CANONICAL_TS),
    )
    return ev.hash


def test_real_audit_entries_validate(
    tmp_path: Path, archive_root: Path
) -> None:
    """End-to-end: ingest one evidence, walk the audit log, validate every
    entry against the schema. Bootstrap entries and ingest entries are
    the two shapes we ship today."""
    blob = tmp_path / "doc.pdf"
    blob.write_bytes(b"%PDF-1.4 schema-test-blob")
    _ingest(archive_root, blob)

    schema = _load_schema(AUDIT_ENTRY_SCHEMA)
    validator = Draft202012Validator(schema)

    for entry in audit_log.iter_entries(archive_root):
        # iter_entries hands back a pydantic model; serialise to the
        # canonical wire dict before validating.
        as_wire = entry.to_canonical_dict()
        validator.validate(as_wire)


